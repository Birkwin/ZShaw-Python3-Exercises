# Create a tab in  the string
tabby_cat = "\tI'm tabbed in."
# Split the string with a new line
persian_cat = "I'm split\non a line."
# Add backslashes as characters in the string
backslash_cat = "I'm \\ a \\ cat."

# Make a large string covering several lines, use tabs and newlines together
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

# Print all the strings we have made
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)