# Print statement to count
print("I will now count my chickens:")

# Both print statements print the string first, and then prints the answer to the
# posted calculation. It automatically puts a space after the OG string

# Division is done first, then subtraction
print("Hens", 25.0 + 30.0 / 6.0)
# Division is done first, and then the modulus, which is the remainder after division.
print("Roosters", 100.0 - 25.0 * 3.0 % 4.0)

print("Now I will count the eggs:")

# Calculates the following equation and prints it out.
# Multiplication and division are done first, and then modulo, and then addition / subtraction.
print(3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 - 1.0 / 4.0 + 6.0)

print("Is it true that 3 + 2 < 5 - 7 ?")

# Prints out 'True' or 'False' depending on the validity of the stated calculation.
print(3.0 + 2.0 < 5.0 - 7.0)

print("What is 3 + 2?", 3.0 + 2.0)
print("What is 5 - 7?", 5.0 - 7.0)

print("Oh, that's why it's False.")

print("How about some more.")

# Like previous print statements, Python will print the results of the statement
# (true / false) after the original string.
print("Is it greater?", 5.0 > -2.0)
print("Is it greater or equal?", 5.0 >= -2.0)
print("Is it less or equal?", 5.0 <= -2.0)
